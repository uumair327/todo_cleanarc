/// Defines the semantic role of a color in the application's design system
enum ColorRole {
  /// Primary surface colors for main backgrounds
  surface,
  /// Text and icon colors that appear on surface colors
  onSurface,
  /// Primary brand colors for key actions and highlights
  primary,
  /// Text and icon colors that appear on primary colors
  onPrimary,
  /// Secondary colors for supporting elements
  secondary,
  /// Text and icon colors that appear on secondary colors
  onSecondary,
  /// Error state colors for warnings and failures
  error,
  /// Text and icon colors that appear on error colors
  onError,
  /// Success state colors for positive feedback
  success,
  /// Text and icon colors that appear on success colors
  onSuccess,
  /// Warning state colors for caution and alerts
  warning,
  /// Text and icon colors that appear on warning colors
  onWarning,
  /// Information state colors for neutral feedback
  info,
  /// Text and icon colors that appear on info colors
  onInfo,
}